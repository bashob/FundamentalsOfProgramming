import java.util.*;

public class Variables {
    public static void main(String[] args){

        // declaring variables
        int marks1;
        int marks2;

        // assigning values and initialization of variables
        marks1 = 72;
        marks2 = 85;

        // declaring variables in a single line
        String student1 = "Aryan";
        String student2 = "Shabana";

        System.out.println(marks1);
        System.out.println(student2);

    }
}
